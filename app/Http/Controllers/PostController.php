    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $posts = Post::with(['category', 'author'])->filter(request(['search', 'category', 'author']))->paginate(3)->withQueryString();
        $title = 'All Posts';
        if(request('category')){
            $title = 'Category : '.strtoupper(request('category'));
        }elseif(request('author')){
            $title = 'author ';
        }
        $view = 'frontend/layouts/posts';
        if($posts->count()<1){
            $view = 'frontend/layouts/noResultPage';
        }
        return view($view,[
            'active' => 'post',
            'title' => $title,
            'posts' => $posts
        ]);
    }

